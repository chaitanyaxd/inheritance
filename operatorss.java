class Operators{
public static void main(String[]args){
int a,b,c,d;
a=2;b=3;c=4;d=5;
System.out.println((a<b)&(a>b));
System.out.println((b<c)&&(b<c));
System.out.println((a<c)^(a>c));
System.out.println(a<=c);
System.out.println((a<d)|(a>d));
System.out.println((a>b)||(a>b));
System.out.println(a!=b);
}

} 